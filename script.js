        // Quick Tabs filter handler
        function filterByTab(tab, element) {
            currentTabFilter = tab;
            
            // Toggle active styling
            document.querySelectorAll('.quick-tab-btn').forEach(btn => {
                btn.classList.remove('active');
            });
            element.classList.add('active');

            // Sync Quick Tab selection with CPT Type Dropdown if appropriate
            if (tab === 'sale') {
                typeDropdown.value = 'sale';
            } else if (tab === 'rent') {
                typeDropdown.value = 'rent';
            } else {
                typeDropdown.value = '';
            }
            
            runCombinedFilters();
        }

        // Combined Filter Runner (Tabs + Area + Type + Budget)
        function runCombinedFilters() {
            let filtered = [...properties];

            // 1. Process Area Location Filter
            const areaVal = locationDropdown.value;
            if (areaVal) {
                filtered = filtered.filter(p => p.acf_fields.location_taxonomy.toLowerCase() === areaVal.toLowerCase());
            }

            // 2. Process Status Type Filter (Dropdown matches input)
            const typeVal = typeDropdown.value;
            if (typeVal) {
                filtered = filtered.filter(p => p.acf_fields.type === typeVal);
                
                // Keep Tab active state aligned
                document.querySelectorAll('.quick-tab-btn').forEach(btn => {
                    btn.classList.remove('active');
                    if (btn.getAttribute('data-tab') === typeVal) {
                        btn.classList.add('active');
                    }
                });
            } else if (currentTabFilter === 'all') {
                // If reset to "All Statuses" dropdown while holding sale/rent tab, clear tab
                document.querySelectorAll('.quick-tab-btn').forEach(btn => {
                    btn.classList.remove('active');
                    if (btn.getAttribute('data-tab') === 'all') {
                        btn.classList.add('active');
                    }
                });
            }

            // 3. Process Tab Override (For Hot Offers special query)
            if (currentTabFilter === 'hot') {
                filtered = filtered.filter(p => p.acf_fields.is_hot === true);
            }

            // 4. Process Budget Numeric Filter
            const budgetVal = budgetDropdown.value;
            if (budgetVal) {
                const maxBudget = parseInt(budgetVal, 10);
                filtered = filtered.filter(p => p.acf_fields.price_value <= maxBudget);
            }

            // Build query description for status badge dropdown
            let queries = [];
            if (areaVal) {
                queries.push(`tax_query: location = "${areaVal}"`);
            }
            if (typeVal) {
                queries.push(`meta_query: type = "${typeVal}"`);
            }
            if (currentTabFilter === 'hot') {
                queries.push(`meta_query: is_hot = true`);
            }
            if (budgetVal) {
                queries.push(`meta_query: price <= ${parseInt(budgetVal, 10).toLocaleString()}`);
            }

            const queryStatusEl = document.getElementById('status-query-type');
            if (queryStatusEl) {
                queryStatusEl.textContent = queries.length > 0 ? queries.join(', ') : 'Default (All Posts)';
                queryStatusEl.title = queries.join('\n');
            }

            renderGrid(filtered);
        }

